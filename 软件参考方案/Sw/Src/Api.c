/*=========================================================================================\n
* @function_name: BCD2ASCII
* @function_file: Api.c
* @描述: BCD 转 ASCII 码
* 
* @param: BCD   
* @param: Ascii  
* @param: len 
* 
* @return: uint8   
* @作者: lwb (2014/2/28)
* @备注:   
*-------------------------------------------------------------------------------------------
* @修改人: 
* @修改内容:  
===========================================================================================*/
uint8 BCD2ASCII(uint8*BCD,uint8*Ascii,uint8 len)
{
    uint8 AsciiLen=0;
	uint8 i;
    for(i=0;i<len;i++)
    {
        Ascii[AsciiLen++]=0x30+((BCD[len-1-i]&0xF0)>>4);
        Ascii[AsciiLen++]=0x30+(BCD[len-1-i]&0x0F);
    }
    return AsciiLen;
}


