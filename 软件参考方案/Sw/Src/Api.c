/*=========================================================================================\n
* @function_name: SumplementNegtive
* @function_file: Api.c
* @描述:    补充完负数据,用于短数据向long型数据扩充
*           比如说一个3字节的负数需要表示成long型的负数,那么long型数据的最高字节位0xff,
*           低三个字节为原来的三个字节
*           限制条件是ucLen在1-3之间
* @参数: 
* @param:ucLen  
* @param:p  
* @返回: 
* @作者:   lwb (2012-02-29)
* @备注: 
*-------------------------------------------------------------------------------------------
* @修改人:  
* @修改内容: 
===========================================================================================*/
void SumplementNegtive(uint8 ucLen, uint8* p)
{
    if(ucLen == 0)
    {
        return;
    }

    if((ucLen == 3) && (*(p+2)&0x80))
    {
        *(p+3) = 0xff;
    }

    if((ucLen == 2) && (*(p+1)&0x80))
    {
        *(p+3) = 0xff;
        *(p+2) = 0xff;
    }

    if((ucLen == 1) && (*(p+0)&0x80))
    {
        *(p+3) = 0xff;
        *(p+2) = 0xff;
        *(p+1) = 0xff;
    }
}
