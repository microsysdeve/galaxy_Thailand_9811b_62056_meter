/*=========================================================================================\n
* @function_name: IntHex2BCD
* @function_file: Api.c
* @描述: 要转换的hex码(hex < 10000)
* 
* 
* @参数: 
* @param:hex  
* 
* @返回: 
* @return: uint16 将hex码转换成bcd之后的结果
* @作者:   lwb (2012-02-29)
* @备注: 
*-------------------------------------------------------------------------------------------
* @修改人:  
* @修改内容: 
===========================================================================================*/
uint16 IntHex2BCD(uint16 hex)
{
    uint16 result = 0;

    if(hex > 9999)
    {
        return 0xFFFF;
    }

    if(hex >= 8000)
    {
        result |= 0x8000;
        hex -= 8000;
    }

    if(hex >= 4000)
    {
        result |= 0x4000;
        hex -= 4000;
    }

    if(hex >= 2000)
    {
        result |= 0x2000;
        hex -= 2000;
    }

    if(hex >= 1000)
    {
        result |= 0x1000;
        hex -= 1000;
    }

    if(hex >= 800)
    {
        result |= 0x800;
        hex -= 800;
    }

    if(hex >= 400)
    {
        result |= 0x400;
        hex -= 400;
    }

    if(hex >= 200)
    {
        result |= 0x200;
        hex -= 200;
    }

    if(hex >= 100)
    {
        result |= 0x100;
        hex -= 100;
    }

    result |= APIByteHEX2BCD(hex);
    return result;

}
 
/*=========================================================================================\n
* @function_name: Hex2BCD
* @function_file: Api.c
* @描述: 要转换的hex码(hex < 100000000)
* 
* 
* @参数: 
* @param:hex  
* 
* @返回: 
* @return: uint32 将hex码转换成bcd之后的结果
* @作者:   lwb (2012-02-29)
* @备注: 
*-------------------------------------------------------------------------------------------
* @修改人:  
* @修改内容: 
===========================================================================================*/
uint32 Hex2BCD(uint32 hex)
{
    uint32 result = 0;
    uint32 cmp, bitcap;
    uint8 uc_i;

    while(hex > 99999999)
    {
        hex-=100000000;
        //return 0xFFFFFFFF;
    }

    for(cmp = 80000000, bitcap = 0x80000000, uc_i = 0; uc_i < 16; uc_i++)
    {
        if(hex >= cmp)
        {
            result |= bitcap;
            hex -= cmp;
        }
        bitcap >>= 1;
        switch(uc_i)
        {
            case 3:
                cmp = 8000000;
                break;
            case 7:
                cmp = 800000;
                break;
            case 11:
                cmp = 80000;
                break;
            default:
                cmp >>= 1;
        }
    }

    result |= IntHex2BCD(hex);
    return result;
}

 
/*=========================================================================================\n
* @function_name: BCD2Hex
* @function_file: Api.c
* @描述: 
* 
* 
* @参数: 
* @param:x  
* 
* @返回: 
* @return: uint32 
* @作者:   lwb (2012-02-29)
* @备注: 
*-------------------------------------------------------------------------------------------
* @修改人:  
* @修改内容: 
===========================================================================================*/
uint32 BCD2Hex(uint32 x)
{
    uint8* p;
    uint32 ulRet = 0;

    p = (uint8*)&x + 3;
    for(uint8 i = 0; i < 4; i++)
    {
        ulRet *= 100;
        ulRet += APIByteBCD2HEX(*p--);
    }

    return ulRet;
}
 
