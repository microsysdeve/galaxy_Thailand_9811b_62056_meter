/*=========================================================================================\n
* @function_name: InitLCD
* @function_file: McuDrive.c
* @描述: 初始化LCD
* 
* @参数: 
* @返回: 
* @作者:   lwb (2012-02-27)
* @备注: 
*-------------------------------------------------------------------------------------------
* @修改人:  
* @修改内容: 
===========================================================================================*/
void InitLCD(void)
{
//LCD显示，必须配置为禁止输入输出
//
    P3OE |= 0x0f;
    P4OE |= 0x03;           // P4.0~P4.1
    P5OE |= 0xff;           // P5.0~P5.7
//    P6OE |= 0x0f;           // P6.0~P6.3
    P7OE |= 0x3f;           // P7.0~P7.5
//    P8OE |= 0x04;           // P8.2 
//   P9OE |= 0x01;           // P9.0
    
    P3IE &=~ 0x0f;
    P4IE &=~ 0x03;          // P4.0~P4.1
    P5IE &=~ 0xff;          // P5.0~P5.7
//    P6IE &=~ 0x0f;          // P6.0~P6.3
    P7IE &=~ 0x3f;          // P7.0~P7.5
//    P8IE &=~ 0x04;          // P8.2 
//    P9IE &=~ 0x01;          // P9.0
    
    LCDG      = 0;
    CtrlLCDV  = 0x00;
    LCDCtrl   = LCDSETTING4COM;     //0x82
    SegCtrl0  = 0x03;
    SegCtrl1  = 0xff;
    SegCtrl2  = 0x00;
    SegCtrl3  = 0x3f;
    SegCtrl4  = 0x00;
}
