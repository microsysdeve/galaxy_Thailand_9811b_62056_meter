void LCD_Off(void)
{
    Disp_Clear();
    SegCtrl0=0;
    SegCtrl1=0;
    SegCtrl2=0;
    SegCtrl3=0;
    SegCtrl4=0;
    DelayXms(5);
    LCDCtrl=0;
    LCDG=1;

}

