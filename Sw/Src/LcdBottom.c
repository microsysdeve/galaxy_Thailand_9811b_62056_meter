void LCD_RAMUpdata(void)
{
    RefreshLCDRAM(guc_LCDRAM);
}

/*=========================================================================================\n
* @function_name: RefreshLCDRAM
* @function_file: DispPlay.c
* @描述: 把缓存显示数据刷新到LCD RAM中
*
*
* @参数：
* @param: p  缓存首地址，该缓存对应的是SEG2到SEG39，每个SEG对应1个字节
*
* @返回：
* @作者: lim (2011-11-02)
* @备注:
*-------------------------------------------------------------------------------------------
* @修改人:
* @修改内容:
===========================================================================================*/
void RefreshLCDRAM(uint8 *p)
{
    uint8x  *pLCDM;

    pLCDM=(uint8x*)&LCDM0;
    *pLCDM = p[0]&0xf8;       //2c00-0  
    
    *(pLCDM+0x04) = p[1];//2c04-1
    *(pLCDM+0x05) = p[2];//2c08-2
    *(pLCDM+0x06) = p[3];//2c09-3
    *(pLCDM+0x07)= p[4];//2c12-4
    *(pLCDM+0x0c)= p[5];//2c13-5
    *(pLCDM+0x0d)= p[6];//2c14-6
    *(pLCDM+0x0e)= p[7];//2c17-7
}